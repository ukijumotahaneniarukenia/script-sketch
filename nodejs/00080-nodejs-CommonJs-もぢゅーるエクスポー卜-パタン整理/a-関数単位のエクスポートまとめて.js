const UnkoMoriMoriMoriOugai = {};

UnkoMoriMoriMoriOugai.unko = (who) => {
  return who + 'うんこ';
}

UnkoMoriMoriMoriOugai.morimori = (unko) => {
  return unko + 'もりもり';
}

UnkoMoriMoriMoriOugai.moriougai = (morimori) => {
  return morimori + '森鴎外';
}

module.exports = UnkoMoriMoriMoriOugai;
